#include "libft.h"

int	ft_memcmp(void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (n == 0)
		return (0);
	while (i + 1 < n && \
		((unsigned char *)s1)[i] == ((const unsigned char *)s2)[i])
		i++;
	return (((unsigned char *)s1)[i] - ((const unsigned char *)s2)[i]);
}
